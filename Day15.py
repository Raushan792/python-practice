# Strind Methods.

# islower - Check into string is a lower case.
print(a,a.islower())
print(b,b.islower())
print(c,c.islower())
print(d,d.islower())

# isupper - check into string upper case.
print(a,a.isupper())
print(b,b.isupper())
print(c,c.isupper())
print(d,d.isupper())
print(e,e.isupper())

# isspace - Return true if all characters in the string are the whitespace.
print(a,a.isspace())
print(b,b.isspace())
print(c,c.isspace())
print(d,d.isspace())
print(e,e.isspace())
print(h,h.isspace())

# istitle - Return true if the string follows rules of the title.
print(d,d.istitle())
print(f,f.istitle())
print(i,i.istitle())
