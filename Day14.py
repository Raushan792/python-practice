# Python Questions.
# String methods.

# isalpha - Return true if all characters in the string in the alphabet.
print(a,a.isalpha())
print(g,g.isalpha())
print(b,b.isalpha())

# isdecimal - Return true if all characters in the string in the decimal.
print(a,a.isdecimal())
print(c,c.isdecimal())
print(e,e.isdecimal())

# isdigit - Return true if all characters in the string in the digit.
print(b,b.isdigit())
print(c,c.isdigit())
