# Python Question.
# String Methods.

# l just - Returns a left justified version of the string.
s = "proud to be Rajputana"
x = s.ljust(8)
print(x,"culture")

# replace - Returns a string where a specified valve is replaced with a
# specified value.
r = "i am a hacker.hacker is so smart"
print(r)
print(r.replace("hacker", "singh"))

# rindex - Search the string for a specified value and returns
# the last position of where it was found.
w = "Roshan Singh is a computer science student"
print(w.rindex("computer science"))

